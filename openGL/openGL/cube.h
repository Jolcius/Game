class Cube
{
private:
    glm::vec3 position;     // 位置
    glm::vec3 rotation;     // 旋转
    glm::vec3 localScale;   // 缩放

    Texture texture;        // 纹理
    glm::vec4 color;        // 颜色
    glm::vec3 velocity;     // 速度

public:
    Cube();
    Cube(glm::vec3 pos);
    Cube(glm::vec3 pos, glm::vec3 rot);
    Cube(glm::vec3 pos, glm::vec3 rot, glm::vec3 scale);

    void setPos(glm::vec3 pos) { position = pos; }
    void setRot(glm::vec3 rot) { rotation = rot; }
    void setScale(glm::vec3 scale) { localScale = scale; }
    void setTexture(Texture tex) { texture = tex; }
    void setColor(glm::vec4 clr) { color = clr; }
    void setVelocity(glm::vec3 v) { velocity = v; }

    glm::vec3 getPos() { return position; }
    glm::vec3 getRot() { return rotation; }
    glm::vec3 getScale() { return localScale; }
    unsigned int getTexture() { return texture.getTexture(); }
    glm::vec4 getColor() { return color; }
    glm::vec3 getVelocity() { return velocity; }
};

